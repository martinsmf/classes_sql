/* RELATÓRIO DE TODOS OS CLIENTE */

SELECT C.NOME, C.CPF, C.SEXO, C.EMAIL,
       T.TIPO, T.NUMERO,
       E.RUA, E.BAIRRO, E.CIDADE, E.ESTADO 
FROM CLIENTE C
INNER JOIN ENDERECO E ON C.IDCLIENTE = E.ID_CLIENTE
INNER JOIN TELEFONE T ON C.IDCLIENTE = T.ID_CLIENTE;

/* RELATÓRIO DE HOMENS */

SELECT NOME, SEXO, EMAIL, ENDERECO FROM CLIENTE WHERE SEXO = 'M';

