       T.TIPO, T.NUMERO,
       E.RUA, E.BAIRRO, E.CIDADE, E.ESTADO 
FROM CLIENTE C
INNER JOIN ENDERECO E ON C.IDCLIENTE = E.ID_CLIENTE
INNER JOIN TELEFONE T ON C.IDCLIENTE = T.ID_CLIENTE
WHERE SEXO = 'M';

SELECT * FROM CLIENTE WHERE IDCLIENTE IN(4, 5, 6,  11);

UPDATE CLIENTE SET SEXO = 'F' WHERE IDCLIENTE IN(4, 5, 6,  11);

